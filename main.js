function replaceMenus(keyword = "") {
  if (!(typeof keyword === 'string' || keyword instanceof String)) {
    keyword = ""
  }
  const menus = document.getElementById("menus");
  const newMenus = []
  data.forEach((e) => {
    if (e.title.indexOf(keyword) === -1) {
      return
    }
    const menu = document.createElement('li');
    const link = document.createElement('a');
    link.textContent = e.title;
    link.href = e.link;
    link.target = "_blank"
    menu.appendChild(link)
    newMenus.push(menu)
  })
  menus.replaceChildren(...newMenus)
}

function search(e) {
  replaceMenus(e.target.value)
}

window.onload = () => {
  const searchText = document.getElementById('searchText');
  searchText.addEventListener('input', search);
  replaceMenus()
}
